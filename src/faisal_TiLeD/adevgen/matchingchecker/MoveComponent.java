private void btnApplyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApplyActionPerformed
    double mx = (Double)spnX.getValue();
    double my = (Double)spnY.getValue();
    
    if(option == 1){
        mx -= curX;
        my -= curY;
        parent.currentComponent.move(mx, my);
    }else if(option == 2){        
        parent.canvas.setCenterX(-(int)mx);
        parent.canvas.setCenterY(-(int)my);
    }
    parent.refresh();
    loadValues();
}//GEN-LAST:event_btnApplyActionPerformed
    
