    private void loadValues(){
        if(option == 1){
            lblLabel.setText("Change Component Position");
            lblComponent.setText(parent.currentComponent.getName());
            curX = parent.currentComponent.left;
            curY = parent.currentComponent.top;
            lblCX.setText("" + curX);
            lblCY.setText("" + curY);  
            spnX.setValue(new Double(curX));
            spnY.setValue(new Double(curY));
        }else if(option == 2){
            lblLabel.setText("Go To ...");
            lblComponent.setText("Current Position");
            curX = -parent.canvas.getCenterX();
            curY = -parent.canvas.getCenterY();
            
            lblCX.setText("" + curX);
            lblCY.setText("" + curY);  
            spnX.setValue(new Double(curX));
            spnY.setValue(new Double(curY));
        }
    }
    
