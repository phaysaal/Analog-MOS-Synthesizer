    public void normalize(){
        int i,j;
        double left,top, right, bottom;
        
        //get left top
        left = vRect.get(0).left;
        top = vRect.get(0).top;
        right = vRect.get(0).right;
        bottom = vRect.get(0).bottom;
        for(i=1; i<vRect.size(); i++){
            if(left > vRect.get(i).left)
                left = vRect.get(i).left;
            if(top > vRect.get(i).top)
                top = vRect.get(i).top;
            
            if(right < vRect.get(i).right)
                right = vRect.get(i).right;
            if(bottom < vRect.get(i).bottom)
                bottom = vRect.get(i).bottom;            
        }
        length = right - left;
        width = bottom - top;
        
        left = -left;
        top = -top;
        for(i=0; i<vRect.size(); i++){
            vRect.get(i).left += left;
            vRect.get(i).right += left;
            vRect.get(i).top += top;
            vRect.get(i).bottom += top;
        }
        
        
        
        System.out.println("------------"+length+","+width);
    }
    
