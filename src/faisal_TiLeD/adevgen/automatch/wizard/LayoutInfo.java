public DrawingComponent getDefaultComponent(){
    DrawingComponent dc = new DrawingComponent("Default");
    int i;
    ComplexRect cr;
    DrawingObject dobj;
    DocumentsHolder dh = DocumentsHolder.getDefaultApp();
    double lambda = DocumentsHolder.lambda;
    for(i=0; i<vRect.size(); i++){
        cr = vRect.get(i);
        dobj = new DrawingObject(cr.left * lambda, cr.top * lambda, (cr.right - cr.left) * lambda, (cr.bottom - cr.top) * lambda, dh.types[cr.layer]);
        dc.element.add(dobj);
    }
    dc.left = 0;
    dc.top = 0;
    dc.length = length * DocumentsHolder.lambda;
    dc.width = width * DocumentsHolder.lambda;
    return dc;
}

private void generateComponentSeries(){
    DrawingComponent dc = getDefaultComponent();
    Vector<DrawingComponent> vDC = new Vector<DrawingComponent>();
    DrawingComponent tdc;
    
    
    int i,j;
    double left=1, top=1;
    double hgap;
    double vgap;
    try{
        hgap = getHorizontalGap();
        vgap = getVerticalGap();
    }catch(Exception e){
        return;
    }
    
    DocumentsHolder.getDefaultApp().newDocuments();
    DocumentsHolder.getDefaultApp().defaultComponent();
    for(i=0; i<matrix.length; i++){
        for(j=0; j<matrix[0].length; j++){
            if(!names[matrix[i][j]].equals("dummy")){
                tdc = dc.clone(names[matrix[i][j]]);
                tdc.move(left * DocumentsHolder.lambda, top * DocumentsHolder.lambda);
                vDC.add(tdc);
            }
            left = left + length + hgap; 
        }
        left = 1;
        top = top + width + vgap;
    }
    
    DocumentsHolder.getDefaultApp().vComp = vDC;
    DocumentsHolder.getDefaultApp().refresh();
    
}

